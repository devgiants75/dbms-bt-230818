### 0824 정리 ###

# 1. order by 
# : 결과가 출력되는 순서를 조절(정렬)
# : 오름차순(asc - 생략 가능), 내림차순(desc) 선택 가능

# 2. limit
# : 출력하는 개수를 제한
# : order by와 주로 함께 사용

# 3. distinct
# : 조회된 결과에서 중복된 것은 1개만 남기는 기능
# : 열 이름 앞에 붙여서 사용

# 4. group by
# : 데이터를 그룹으로 묶어주는 기능

# 5. having
# : 집계 함수와 관련된 조건을 제한
# : group by 다음에 작성
